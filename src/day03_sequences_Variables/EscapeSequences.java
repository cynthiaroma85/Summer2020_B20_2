package day03_sequences_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone \nMy name is Cybertek"); //this print mean \n New Line
        System.out.println("\tIm in Virginia");// this print means \t new paragraph
        System.out.println("\n\n\t\tI Love Programming");

        System.out.println("\\"); //this print means ONE backlash
        System.out.println("\\\\"); //this print means TWO backlash
        System.out.println("/"); //this mean one Frontlash
        System.out.println("//"); // this print means two frontlash

        System.out.println("\"My name is Cynthia\"");
        System.out.println("'My name is Cynthia'");




    }

}
